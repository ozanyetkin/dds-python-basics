# I will define two variables called a and b
# I will ask a and b to the user
# I will add, subtract, multiply and divide a and b
# I will print the results
a = int(input("Please, give me a number: "))
b = int(input("Please, give me another number: "))

print(f"The summation of {a} and {b} is {a + b}")
print("The difference of", a, "and", b, "is", a - b)
print("The product of", a, "and", b, "is", a * b)
print("The division of", a, "and", b, "is", a / b)
